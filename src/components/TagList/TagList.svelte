<script lang="ts">
  import { IconButton, Tag } from "obsidian-svelte";

  import { app } from "src/lib/stores/obsidian";
  import { InputDialogModal } from "src/modals/input-dialog";

  export let values: string[];
  export let edit: boolean;

  export let onChange: (values: string[]) => void = () => {};
</script>

<div class:edit>
  {#if edit}
    {#each values as value, i}
      <Tag>
        {value}
        <IconButton
          icon="cross"
          size="xs"
          nopadding
          on:click={() => {
            onChange(values.filter((_, j) => i !== j));
          }}
        />
      </Tag>
    {/each}
    <IconButton
      icon="plus"
      nopadding
      on:click={() => {
        new InputDialogModal($app, "Add list item", "Add", (value) => {
          onChange([...values, value]);
        }).open();
      }}
    />
  {:else}
    {#each values as value}
      <Tag>{value}</Tag>
    {/each}
  {/if}
</div>

<style>
  div {
    display: flex;
    align-items: center;
    gap: 4px;
    overflow: hidden;
    padding: 4px;
  }

  .edit {
    flex-wrap: wrap;
  }
</style>
